def seed():
    db = SessionLocal()
    try:
        # Категория "Готовые промпты" (на будущее — для простых шаблонов)
        cat_prompts = db.query(SolutionCategory).filter_by(slug="prompts").first()
        if not cat_prompts:
            cat_prompts = SolutionCategory(slug="prompts", title="Готовые промпты", sort_order=1)
            db.add(cat_prompts); db.commit(); db.refresh(cat_prompts)
            print(f"✓ Категория 'Готовые промпты' создана (id={cat_prompts.id})")

        # Категория "Бизнес-решения"
        cat = db.query(SolutionCategory).filter_by(slug="business").first()
        if not cat:
            cat = SolutionCategory(slug="business", title="Бизнес-решения", sort_order=2)
            db.add(cat); db.commit(); db.refresh(cat)
            print(f"✓ Категория 'Бизнес-решения' создана (id={cat.id})")
        else:
            print(f"— Категория 'Бизнес-решения' уже есть (id={cat.id})")

        added = 0
        updated = 0
        for i, p in enumerate(BUSINESS_PROMPTS, 1):
            new_price = price_for(p["title"])
            tier = _tier_for(p["title"])
            existing = db.query(Solution).filter_by(title=p["title"]).first()
            if existing:
                # Обновляем цену если поменялся тариф
                if existing.price_tokens != new_price:
                    old = existing.price_tokens
                    existing.price_tokens = new_price
                    db.commit()
                    print(f"  {i:2}. ↻ {tier:<6} {old:>3}→{new_price:<3} CH  {p['title']}")
                    updated += 1
                else:
                    print(f"  {i:2}.   {tier:<6}     {new_price:<3} CH  {p['title']}")
                continue

            sol = Solution(
                category_id=cat.id,
                title=p["title"],
                description=f"[{p['cat']}] {p['description']}",
                price_tokens=new_price,
                is_active=True,
                sort_order=i,
            )
            db.add(sol); db.commit(); db.refresh(sol)

            step = SolutionStep(
                solution_id=sol.id,
                step_number=1,
                title=p["title"],
                model="claude",                 # Claude Sonnet по заданию
                system_prompt="Ты профессиональный бизнес-консультант. Отвечай развёрнуто, по делу, на русском языке. Используй markdown-форматирование (заголовки, списки, таблицы) для удобства чтения.",
                user_prompt=p["prompt"],
                wait_for_user=True,
                user_hint=p["hint"],
            )
            db.add(step); db.commit()
            print(f"  {i:2}. + {tier:<6}     {new_price:<3} CH  {p['title']}")
            added += 1

        print(f"\n✅ Готово: добавлено {added}, обновлено {updated}, всего {len(BUSINESS_PROMPTS)}")
        print(f"   Тарифы: light={TIER_PRICES['light']} CH, medium={TIER_PRICES['medium']} CH, heavy={TIER_PRICES['heavy']} CH")
    finally:
        db.close()


if __name__ == "__main__":
    seed()
